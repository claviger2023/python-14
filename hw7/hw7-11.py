TRANSLIT_DICT = {}

def sequence_buttons(string):

    for c, l in zip(SYMBOLS, NUMBERS):
        TRANSLIT_DICT[ord(c)] = l
        TRANSLIT_DICT[ord(c.upper())] = l.upper()

    result = string.translate(TRANSLIT_DICT)

    return result